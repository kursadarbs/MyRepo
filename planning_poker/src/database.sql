	"EST_COMMENT" VARCHAR2(500 CHAR), 
	"UPDATE_TIME" TIMESTAMP (6), 
	"EST_VALUE" NUMBER(10,0), 
	"REQUIREMENT_ID" NUMBER(19,0), 
	"USER_ID" NUMBER(19,0),
	constraint ESTIMATIONS_HISTORY_PK primary key (ID),
	constraint ESTIMATIONS_HIS_USER_ID_FK foreign key (USER_ID) references USERS(ID),
