	constraint ESTIMATIONS_USER_ID_FK foreign key (USER_ID) references USERS(ID),
	constraint ESTIMATIONS_REQUIREMENT_ID_FK foreign key (REQUIREMENT_ID) references REQUIREMENTS(ID)
   );

